self.addEventListener('install', function (event) {
  event.waitUntil(
    caches.keys().then(function (names) {
      var validSets = ["is-sw-f7686c","is-html-f7686c","is-jsdelivr-f7686c","is-gtm-f7686c","is-gravatar-f7686c","is-theme-f7686c","is-cdn-f7686c","is-json-f7686c","is-custom-f7686c","is-img-f7686c"];
      return Promise.all(
        names
          .filter(function (name) { return !~validSets.indexOf(name); })
          .map(function (name) {
            indexedDB && indexedDB.deleteDatabase(name);
            return caches.delete(name);
          })
      ).then(function() { self.skipWaiting() });
    })
  );
});

